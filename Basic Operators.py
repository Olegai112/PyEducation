# Целые числа
a = 10
b = 5
print(a + b)  # Вывод: 15
print(a - b)  # Вывод: 5
print(a * b)  # Вывод: 50
print(a / b)  # Вывод: 2.0
print(a // b)  # Вывод: 3
print(a % b)   # Вывод: 1
print(a ** b)   # Вывод: 100000
print(a == b)  # Вывод: False
print(a != b)  # Вывод: True
print(a > b)   # Вывод: True
print(a < b)   # Вывод: False
print(a >= b)  # Вывод: True
print(a <= b)  # Вывод: False